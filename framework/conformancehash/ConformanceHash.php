    public function findNodeByValue($value)
    {
        if ($this->_length == 1) {
            return $this->_node;
        }
        $value = crc32($value) % (2 << 32);
        $node = $this->_node;
        while ($node) {
            if ($node->_next->_isFirst) {
                return $node->_next;
            }
            if ($value >= $node->_value && $value < $node->_next->_value) {
                return $node->_next;
            } else {
                $node = $node->_next;
            }
        }

        return $node;
    }
